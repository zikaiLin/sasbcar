#' Normalize an image.
#'
#' This function normalizes an image by dividing each pixel value by the maximum possible value (255).
#'
#' @param image A matrix representing an image.
#' @return A matrix representing the normalized image.
#' @export
#' @examples
#' img <- matrix(runif(100, min = 0, max = 255), ncol = 10)
#' normalized_img <- normalized_image(img)


normalized_image <- function(image) {
  # Normalize the image
  return(image / 255)
}